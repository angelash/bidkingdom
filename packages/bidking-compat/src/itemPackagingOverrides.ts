function headDisplayName(row: Pick<BidKingRawTableRow, 'id' | 'table'>): string | undefined {
  if (row.table.toLowerCase() !== 'head') {
    return undefined;
  }
  const prestigeName = BID_KING_PRESTIGE_HEAD_NAMES[row.id];
  if (prestigeName) {
    return prestigeName;
  }
  const match = /^12(\d{2})(\d{2})$/.exec(row.id);
  if (!match) {
    return undefined;
  }
  const heroCode = Number(match[1]);
  const variant = Number(match[2]);
  const heroName = heroHeadBaseName(heroCode);
  const variantName = heroHeadVariantName(variant);
  return heroName ? `${heroName}${variantName}头像` : undefined;
}

